def _normalize_venue_name(value: str) -> str:
    return value.strip().lower()


MLB_PARKS_BY_HOME_TEAM_ID: dict[int, ParkData] = {
    park["home_team_id"]: park
    for park in MLB_PARKS
    if park.get("home_team_id") is not None
}

MLB_PARKS_BY_VENUE_NAME: dict[str, ParkData] = {
    _normalize_venue_name(venue_name): park
    for park in MLB_PARKS
    for venue_name in park["venue_names"]
}


def get_park_by_team_or_venue(
    home_team_id: int | None, venue_name: str | None
) -> ParkData | None:
    if venue_name:
        park = MLB_PARKS_BY_VENUE_NAME.get(_normalize_venue_name(venue_name))
        if park is not None:
            return park
    if home_team_id is None:
        return None
    return MLB_PARKS_BY_HOME_TEAM_ID.get(home_team_id)
